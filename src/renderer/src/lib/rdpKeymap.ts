// Maps a browser KeyboardEvent.code to an RDP (set 1 / "scancode set 1") scan
// code plus whether it's an extended key (sent with KBD_FLAGS_EXTENDED). The
// sidecar forwards these straight to freerdp_input_send_keyboard_event, so all
// the layout knowledge lives here rather than in native code.
//
// Using event.code (physical key, layout-independent) rather than event.key
// keeps this a fixed table; the remote applies its own keyboard layout.

export interface RdpKey {
  scancode: number
  extended: boolean
}

const k = (scancode: number, extended = false): RdpKey => ({ scancode, extended })

